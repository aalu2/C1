
#include <stdio.h>

int main() {
    float celsius = 32, fahrenheit;  // Declaring variables for celsius and fahrenheit and assigning the value 32

    fahrenheit = 1.8 * celsius + 32;  // Convert celsius to fahrenheit using the formula fahrenheit = 1.8 * celsius + 32

    printf("Temperature in Fahrenheit: %.2f", fahrenheit);  // Printing the result in Fahrenheit 

    return 0;
}
